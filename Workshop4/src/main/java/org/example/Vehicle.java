package org.example;

public class Vehicle {

    private int vin;
    private int year;
    private String make;
    private String model;
    private String vehicleType;
    private String color;
    private int odometer;
    private double price;

    public Vehicle(int vin, int year, String make, String model, String vehicleType, String color, int odometer, double price) {
        this.vin = vin;
        this.year = year;
        this.make = make;
        this.model = model;
        this.vehicleType = vehicleType;
        this.color = color;
        this.odometer = odometer;
        this.price = price;
    }

    public String toCSV() {
        return String.join("|",
                String.valueOf(vin),
                String.valueOf(year),
                make,
                model,
                vehicleType,
                color,
                String.valueOf(odometer),
                String.valueOf(price)
        );
    }

    public static Vehicle fromCSV(String csvLine) {
        String[] parts = csvLine.split("\\|");
        return new Vehicle(
                Integer.parseInt(parts[0]),  // VIN
                Integer.parseInt(parts[1]),  // Year
                parts[2],                   // Make
                parts[3],                   // Model
                parts[4],                   // Vehicle Type
                parts[5],                   // Color
                Integer.parseInt(parts[6]),  // Odometer
                Double.parseDouble(parts[7]) // Price
        );
    }

    public int getVin() {
        return vin;
    }

    public void setVin(int vin) {
        this.vin = vin;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getMake() {
        return make;
    }

    public void setMake(String make) {
        this.make = make;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getVehicleType() {
        return vehicleType;
    }

    public void setVehicleType(String vehicleType) {
        this.vehicleType = vehicleType;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getOdometer() {
        return odometer;
    }

    public void setOdometer(int odometer) {
        this.odometer = odometer;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
}
